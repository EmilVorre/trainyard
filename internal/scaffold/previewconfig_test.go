func TestRenderPreviewConfig_ServiceWithImage(t *testing.T) {
	cfg := DefaultConfig()
	cfg.Stack = StackAppDB
	cfg.Services = ServicesForStack(cfg)

	out := RenderPreviewConfig(cfg)

	// db service uses image, not build
	if !strings.Contains(out, "image: postgres:16-alpine") {
		t.Error("expected postgres image in output")
	}
	// image services should not have a build block
	lines := strings.Split(out, "\n")
	inDBService := false
	for _, line := range lines {
		if strings.Contains(line, "name: db") {
			inDBService = true
		}
		if inDBService && strings.Contains(line, "build:") {
			t.Error("db service with image should not have build block")
			break
		}
		// next service resets context
		if inDBService && strings.HasPrefix(strings.TrimSpace(line), "- name:") && !strings.Contains(line, "db") {
			break
		}
	}
}

