func TestRenderPreviewConfig_PrivateServiceNoSubdomain(t *testing.T) {
	cfg := DefaultConfig()
	cfg.Stack = StackFrontendBackend
	cfg.Services = ServicesForStack(cfg)

	out := RenderPreviewConfig(cfg)

	// backend is private — should not have subdomain
	lines := strings.Split(out, "\n")
	inBackend := false
	for _, line := range lines {
		if strings.Contains(line, "name: backend") {
			inBackend = true
		}
		if inBackend && strings.Contains(line, "subdomain:") {
			t.Error("private backend service should not have subdomain")
			break
		}
		if inBackend && strings.Contains(line, "name: frontend") {
			break
		}
	}
}

